    {
        $this->validate($request, [
            'comment' => 'required',
        ]);
        try {
            $this->comment_model->addRecord($request, $story_id);
        } catch (\Exception $e) {
            $errorcd = 'E5004';
            \Log::error(\Lang::get("errors.{$errorcd}"), [$e]);
            return redirect('/error')->with([
                'errorcd' => $errorcd,
                'errormsg' => \Lang::get("errors.{$errorcd}"),
            ]);
        }
        return redirect()->back();
    }

